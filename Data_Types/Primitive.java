package Data_Types;

public class Primitive {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int sum = a + b;
        System.out.println("The sum of two numbers is " + sum);

        int diff = b - a;
        System.out.println("The difference between two given numbers is " + diff);
    }
}
